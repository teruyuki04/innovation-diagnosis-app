def interpret_score(score):
    if score <= 16:
        return (
            "🔴【要基盤構築フェーズ】\n"
            "イノベーションの構造がほぼ存在せず、現場の努力に依存しています。\n"
            "まずは「問い方と勝ち方」の仕組みを整備すべき段階です。\n"
            "👉 たとえば：全社横断の仮説構築・検証ワークショップの実施、共通テンプレートの導入"
        )
    elif score <= 19:
        return (
            "🟡【局所最適フェーズ】\n"
            "部分的な仕組みはあるが、全社としては定着していません。\n"
            "取り組みの質とスケールにムラがある状態です。\n"
            "👉 たとえば：部門横断のプロセス標準化プロジェクト、新規事業制度の再設計"
        )
    else:
        return (
            "🟢【飛躍準備フェーズ】\n"
            "構想の土台はあり、次は『共通言語化』や『人材と制度の接続』で\n"
            "全社的な飛躍が可能な段階です。\n"
            "👉 たとえば：育成プログラム設計、ステージゲート制の導入、外部との連携強化"
        )
